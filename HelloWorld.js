var testScore = 76;
var grade;
if (testScore >= 90){
    grade = "A";
}
else if(testScore >= 80){
    grade = "B";
}
else if(testScore >= 70){
    grade = "C";
}
else if(testScore >= 60){
    grade = "D";
}
else{
    grade = "E";
}

console.log("Grade =" + grade);
